# Esta Variable es un número
i = 5
print(i)

# Podemos hacer operaciones con variables
i = i + 1
print(i)

# Esta Variable es un texto (o 'Constante literal')
s = '''Esto es un texto de varias líneas.
También funciona con una sola línea.
En ese caso usaríamos comillas dobles o simples'''
print(s)

'''Las variables de tipo texto se conocen como Constantes literales
porque su valor no varía, sino que es constante y es literal porque
es un texto '''


